int CECCPrivateKey::GenerateKey(const unsigned char *pRandomUser, int iLenOfRandom)
{
	if (pRandomUser)
	{
		if (iLenOfRandom > g_paramFieldP.GetLengthInBytes())
			return 0;	// error

		m_paramD.Import(pRandomUser, iLenOfRandom);
		while (!(m_paramD < g_paramN.m_oModulus))
			m_paramD -= g_paramN.m_oModulus;

		if (m_paramD == 0)
			return 0;	// error
	}

	// 
	MultiplyGByTable(m_pntPx, m_pntPy, m_paramD);

	if (m_pntPy == 0)
		return 0;
		
	m_inverseDplus1 = (unsigned int)0;
	return 1;		// ok
}


