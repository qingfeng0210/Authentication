void CEllipticCurve::Jacobian2Stand(CMpi &x, CMpi &y, CMpi &z)		// 
{
	// x = x/(z*z)
	// y = y/(z*z*z)
	CMpi t, t2;
	t = g_paramFieldP.BinaryInverse(z);

	CMpl lltmp = t.FastSquare();

	t2 = (lltmp %= g_paramFieldP.m_oModulus);

	lltmp = x * t2;
	x = (lltmp %= g_paramFieldP.m_oModulus);

	lltmp = t * t2;
	t2 = (lltmp %= g_paramFieldP.m_oModulus);
	lltmp = t2 * y;
	y = (lltmp %= g_paramFieldP.m_oModulus);
}



void CEllipticCurve::DoubleMplJacobian(CMpi &x, CMpi &y, CMpi &z)
{
	if (x == 1
		&& y == 1
		&& z == 0)		// point at infinity
	{
		return;
	}

	CMpl lltmp = x.FastSquare();
	CMpl lda1 = (lltmp %= g_paramFieldP.m_oModulus);

//	lda1 = lda1.l * 3;
	lda1.BitShiftLeft(1);
	lda1 += lltmp.l;

	while (lda1.h != 0)
		lda1 -= g_paramFieldP.m_oModulus;
	while (!(lda1.l < g_paramFieldP.m_oModulus))
		lda1.l -= g_paramFieldP.m_oModulus;

//	lltmp = z*z;
	lltmp = z.FastSquare();
	lltmp %= g_paramFieldP.m_oModulus;
//	lltmp = lltmp.l * lltmp.l;
	lltmp = lltmp.l.FastSquare();
	lltmp %= g_paramFieldP.m_oModulus;

	lltmp = lltmp.l * g_paramA;
	lltmp %= g_paramFieldP.m_oModulus;
	lda1 += lltmp.l;
	while (lda1.h  != 0)
		lda1 -= g_paramFieldP.m_oModulus;
	while (!(lda1.l < g_paramFieldP.m_oModulus))
		lda1.l -= g_paramFieldP.m_oModulus;
	// lambda1, ok

	CMpl lda2 = y.FastSquare();
	lda2 %= g_paramFieldP.m_oModulus;
//	lltmp = lda2.l * lda2.l;
	lltmp = lda2.l.FastSquare();
	lltmp %= g_paramFieldP.m_oModulus;

	lltmp.BitShiftLeft(3);
//	CMpi lda3 = (lltmp %= g_paramFieldP.m_oModulus);		// lambda3, ok

	while (lltmp.h != 0)
		lltmp -= g_paramFieldP.m_oModulus;
	while (!(lltmp.l < g_paramFieldP.m_oModulus))
		lltmp.l -= g_paramFieldP.m_oModulus;
	CMpi lda3 = lltmp.l;

	lda2 = x * lda2.l;
	lda2 %= g_paramFieldP.m_oModulus;
	lda2.BitShiftLeft(2);
	while (lda2.h  != 0)
		lda2 -= g_paramFieldP.m_oModulus;
	while (!(lda2.l < g_paramFieldP.m_oModulus))
		lda2.l -= g_paramFieldP.m_oModulus;	// lambda2, ok

	// x
//	lltmp = lda1.l * lda1.l;
	lltmp = lda1.l.FastSquare();
	x = (lltmp %= g_paramFieldP.m_oModulus);

	lltmp = lda2;
	lltmp.BitShiftLeft(1);
	while (lltmp.h  != 0)
		lltmp -= g_paramFieldP.m_oModulus;
	while (!(lltmp.l < g_paramFieldP.m_oModulus))
		lltmp.l -= g_paramFieldP.m_oModulus;
	x -= lltmp.l;
	if (x.IsNegative())
		x += g_paramFieldP.m_oModulus;

	// y
	lda2 -= x;
	lltmp = lda1.l * lda2.l;
	lltmp %= g_paramFieldP.m_oModulus;
	lltmp -= lda3;
	lda1.l = y;		// just for store
	y = (lltmp %= g_paramFieldP.m_oModulus);
	if (y.IsNegative())
		y += g_paramFieldP.m_oModulus;

	// z
	lltmp = lda1.l * z;
	lltmp %= g_paramFieldP.m_oModulus;
	lltmp.BitShiftLeft(1);
	while (lltmp.h  != 0)
		lltmp -= g_paramFieldP.m_oModulus;
	while (!(lltmp.l < g_paramFieldP.m_oModulus))
		lltmp.l -= g_paramFieldP.m_oModulus;
	z = lltmp;
}

bool CEllipticCurve::CheckPoint(const CMpi &x, const CMpi &y)
{
	if (x.IsNegative() || y.IsNegative()
		|| (x > (g_paramFieldP.m_oModulus-1)) || (y > (g_paramFieldP.m_oModulus-1)))
		return false;

	CMpl left = x.FastSquare();
	left += g_paramA;
	left %= g_paramFieldP.m_oModulus;

	left = left.l * x;
	left += g_paramB;
	left %= g_paramFieldP.m_oModulus;

	CMpl right = y.FastSquare();
	right %= g_paramFieldP.m_oModulus;

	if (right.l == left.l)
		return true;
	else
		return false;
}

