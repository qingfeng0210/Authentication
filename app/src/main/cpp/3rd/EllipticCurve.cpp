	m_pntPx.Import(pKey, g_paramFieldP.GetLengthInBytes());
	m_pntPy.Import(pKey+g_paramFieldP.GetLengthInBytes(), g_paramFieldP.GetLengthInBytes());

	return CheckPoint(m_pntPx, m_pntPy);
