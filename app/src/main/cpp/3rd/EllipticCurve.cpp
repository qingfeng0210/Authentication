int CEllipticCurve::KeyExchangeRndMsg(unsigned char *pbRndMsg, const unsigned char *pbRnd, int iLenOfRnd)
{
	CMpi rnd, x, y;

	if (!rnd.Import(pbRnd, iLenOfRnd))
		return 0;	// error

	if ((rnd == 0) || (rnd == g_paramN.m_oModulus))
		return 0;	// error

	if (rnd > g_paramN.m_oModulus)
		rnd -= g_paramN.m_oModulus;

	MultiplyGByTable(x, y, rnd);
	if (y == 0)
		return 0;

	int iRet = x.Export(pbRndMsg, g_paramFieldP.GetLengthInBytes());
	return iRet += y.Export(pbRndMsg+iRet, g_paramFieldP.GetLengthInBytes());
}

void CEllipticCurve::DoubleMplJacobian(CMpi &x2, CMpi &y2, CMpi &z2, const CMpi &x, const CMpi &y, const CMpi &z)
{
	x2 = x;
	y2 = y;
	z2 = z;
	DoubleMplJacobian(x2, y2, z2);
}

const CMpi *CEllipticCurve::GetN()
{
	return &g_paramN.m_oModulus;
}


